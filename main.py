ALLOWED_TYPES = {"image/jpeg", "image/png", "image/gif", "image/webp"}


@app.post("/analyze")
async def analyze(file: UploadFile = File(...)):
    if file.content_type not in ALLOWED_TYPES:
        raise HTTPException(400, "仅支持 JPG/PNG/GIF/WebP 格式")

    contents = await file.read()
    if len(contents) > 20 * 1024 * 1024:
        raise HTTPException(400, "图片不能超过 20MB")

    api_key = os.environ.get("DASHSCOPE_API_KEY")
    if not api_key:
        raise HTTPException(500, "未配置 DASHSCOPE_API_KEY，请检查环境变量")

    media_type = file.content_type or "image/jpeg"
    b64 = base64.standard_b64encode(contents).decode("utf-8")

    client = OpenAI(
        api_key=api_key,
        base_url="https://dashscope.aliyuncs.com/compatible-mode/v1",
    )

    response = client.chat.completions.create(
        model="qwen-vl-max",
        max_tokens=2000,
        temperature=0,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {
                "role": "user",
                "content": [
                    {
                        "type": "image_url",
                        "image_url": {"url": f"data:{media_type};base64,{b64}"},
                    },
                    {"type": "text", "text": USER_PROMPT},
                ],
            },
        ],
    )

    text = response.choices[0].message.content.strip()
    # 清洗 markdown 代码块
    if text.startswith("```"):
        parts = text.split("```")
        if len(parts) >= 2:
            text = parts[1]
            if text.startswith("json"):
                text = text[4:]
        text = text.strip()

    try:
        result = json.loads(text)
    except json.JSONDecodeError:
        raise HTTPException(500, f"模型返回格式异常：{text[:300]}")

    # 兼容旧格式 unconfirmed（字符串数组 → 对象数组）
    if result.get("unconfirmed") and isinstance(result["unconfirmed"][0], str):
        result["unconfirmed"] = [
            {"observation": s, "suggestion": "建议人工复核"} for s in result["unconfirmed"]
        ]

    return JSONResponse(result)


